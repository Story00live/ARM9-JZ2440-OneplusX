.text
.global _start

_start:
		/* -设置内存: 栈SP- */
		ldr		sp,		=4096										/* Nand启动 */
//		ldr		sp,		=0x40000000+4096								/* Nor启动 */
		
		/* -调用main函数- */
		bl		main
		
halt:
		/* -死循环- */
		b		halt
		
