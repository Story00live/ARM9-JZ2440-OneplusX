#include "i2c.h"

/**********************************************************************************************************
 @Function			int I2C_Init(void)
 @Description			I2C_Init						: I2C初始化
 @Input				void
 @Return				-1							: Error
					0							: Success
**********************************************************************************************************/
int I2C_Init(void)
{
	/* 注册I2C控制器 */
	I2C_Controller_add();
	
	/* 选择I2C控制器 */
	if (I2C_Controller_select("s3c2440") == -1) return -1;
	
	/* 初始化I2C控制器 */
	if (I2C_Controller_init() == -1) return -1;
	
	return 0;
}

/**********************************************************************************************************
 @Function			int I2C_Transfer(p_i2c_msg msgs, int num)
 @Description			I2C_Transfer					: I2C数据传输
 @Input				msgs							: 消息
					num							: 消息号
 @Return				-1							: Error
					0							: Success
**********************************************************************************************************/
int I2C_Transfer(p_i2c_msg msgs, int num)
{
	return I2C_Controller_transfer(msgs, num);
}

