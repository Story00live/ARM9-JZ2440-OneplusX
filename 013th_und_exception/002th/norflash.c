/**********************************************************************************************************
 @Function			void S3C2440_NorFlashInitialized(void)
 @Description			S3C2440_NorFlashInitialized
 @Input				void
 @Return				void
**********************************************************************************************************/
void S3C2440_NorFlashInitialized(void)
{
	BANKCON0 |=  (7<<8);
	BANKCON0 &= ~(2<<8);
}

