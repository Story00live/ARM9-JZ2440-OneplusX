/**********************************************************************************************************
 @Function			void S3C2440_KeyInitialized(void)
 @Description			S3C2440_KeyInitialized
 @Input				void
 @Return				void
**********************************************************************************************************/
void S3C2440_KeyInitialized(void)
{
	/* -配置GPF0,GPF2,GPG3,GPG11为输入引脚- */
	GPFCON &= ~((3<<(0*2)) | (3<<(2*2)));
	GPFCON |=  ((0<<(0*2)) | (0<<(2*2)));
	
	GPGCON &= ~((3<<(3*2)) | (3<<(11*2)));
	GPGCON |=  ((0<<(3*2)) | (0<<(11*2)));
}

