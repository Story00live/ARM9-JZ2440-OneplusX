.text
.global _start

_start:
		/* -关闭看门狗- */
		ldr		r0,		=0x53000000
		ldr		r1,		=0x00000000
		str		r1,		[r0]
		
		/* -设置内存: 栈SP- */
		ldr		sp,		=4096										/* Nand启动 */
//		ldr		sp,		=0x40000000+4096								/* Nor启动 */
		
		/* -传递参数给执行函数- */
		mov		r0,		#1
		/* -调用main函数- */
		bl		led_on
		
		/* -传递参数给执行函数- */
		ldr		r0,		=0x0005FFFF
		/* -简单延时函数- */
		bl		delay
		
		/* -传递参数给执行函数- */
		mov		r0,		#0
		/* -调用main函数- */
		bl		led_on
		
halt:
		/* -死循环- */
		b		halt
		
