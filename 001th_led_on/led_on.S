  *					 点亮nLED_4 : GPF6
  *					 配置GPF4,GPF5,GPF6为输出引脚
  *					 设置GPF4,GPF5,GPF6输出高电平
  *********************************************************************************************************
  */

.text
.global _start

_start:
		/* -配置GPF4,GPF5,GPF6为输出引脚-
		 * -把 0x00001500 写到地址 0x56000050-
		 */
		ldr		r1,		=0x56000050
		ldr		r0,		=0x00001500
		str		r0,		[r1]
		
		/* -配置GPF4,GPF5,GPF6输出低电平-
		 * -把 0x00000000 写到地址 0x56000054-
		 */
		ldr		r1,		=0x56000054
		ldr		r0,		=0x00000000
		str		r0,		[r1]
		
halt:
		/* -死循环- */
		b		halt
		
		
		
		
